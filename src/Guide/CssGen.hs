{-# LANGUAGE OverloadedStrings #-}

{- | Generate public/brand.css — the canonical CSS design system file.
Consumer projects copy this file locally and @import it in their CSS.
Note: Tailwind v4 requires @theme to be in the locally-processed file;
importing from a CDN URL does not propagate @theme tokens.
-}
module Guide.CssGen (generateBrandCss) where

import Data.Maybe (mapMaybe)
import Data.Text (Text)
import Data.Text qualified as T
import Guide.Types
import Numeric (showFFloat)

generateBrandCss :: DesignGuide -> Text
generateBrandCss dg =
    T.concat
        [ cssHeader
        , cssFontFace (dgTypography dg)
        , cssTheme dg
        , cssTypeUtilities (dgTypography dg)
        , cssBaseLayer
        , cssReducedMotion
        , cssSharedComponents
        ]

-- ---------------------------------------------------------------------------
-- Header
-- ---------------------------------------------------------------------------

cssHeader :: Text
cssHeader =
