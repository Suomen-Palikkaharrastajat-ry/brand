        [ "/* brand.css \x2014 Suomen Palikkaharrastajat ry design system"
        , " * Generated by brand-gen. Consumer projects import this file locally."
        , " * Do NOT import from a CDN URL \x2014 Tailwind v4 requires @theme to be in the"
        , " * locally processed file. Copy this file and import it in your project CSS."
        , " */"
        , ""
        ]

-- ---------------------------------------------------------------------------
