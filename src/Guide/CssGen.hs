fmtD :: Double -> Text
fmtD d = T.pack (stripZeros (showFFloat (Just 6) d ""))
  where
    stripZeros s
        | '.' `elem` s = reverse . dropWhile (== '.') . dropWhile (== '0') . reverse $ s
        | otherwise = s
