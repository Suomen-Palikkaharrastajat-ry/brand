{-# LANGUAGE OverloadedStrings #-}

{- | Generate src/Guide/Tokens.elm — the single Elm source of truth for all
brand design constants. Modify design-guide.toml, then run @make run@ to
regenerate this file and design-guide.tokens.json together.
-}
module Guide.ElmGen (generateBrandModule) where

import Data.Text (Text)
import Data.Text qualified as T
import Guide.Types

generateBrandModule :: DesignGuide -> Text
generateBrandModule dg =
    let m = dgMeta dg
        tc = dgTypography dg
        sp = dgSpacing dg
        mc = dgMotion dg
        fontStack = T.intercalate ", " (tcFontFamily tc)
     in T.unlines $
            [ "module Guide.Tokens exposing (..)"
            , ""
            , "{-| Generated brand design constants for " <> metaOrganization m <> "."
            , "Do not edit by hand \x2014 regenerate with `make run` from the design-guide repo."
            , "-}"
            , ""
            , "-- Generated by brand-gen. Do not edit by hand."
            , "-- Single Elm source for all brand design constants."
            , "-- To modify: change design-guide.toml, then run `make run`."
            , "-- ---------------------------------------------------------------------------"
            , "-- Organisation"
            , "-- ---------------------------------------------------------------------------"
            , ""
            , ""
            , "associationName : String"
            , "associationName ="
            , "    " <> q (metaOrganization m)
            , ""
            , ""
            , "logoBaseUrl : String"
            , "logoBaseUrl ="
            , "    " <> q (metaCanonicalUrl m)
            , ""
            , ""
            , "designGuideUrl : String"
            , "designGuideUrl ="
            , "    " <> q "/design-guide.tokens.json"
            , ""
            , ""
            , ""
            , "-- ---------------------------------------------------------------------------"
            , "-- Brand colors (primitive)"
            , "-- ---------------------------------------------------------------------------"
            , ""
            , ""
            , "colorBrandBlack : String"
            , "colorBrandBlack ="
            , "    " <> q (hexText (metaFeatureColor m))
            , ""
            , ""
            , "colorBrandWhite : String"
            , "colorBrandWhite ="
            , "    " <> q (hexText (metaHighlightColor m))
            , ""
            , ""
            , "colorBrandYellow : String"
            , "colorBrandYellow ="
            , "    " <> q "#FAC80A"
            , ""
            , ""
            , ""
            , "-- ---------------------------------------------------------------------------"
            , "-- Semantic colors"
            , "-- ---------------------------------------------------------------------------"
            ]
                ++ concatMap renderSemanticColor (dgSemanticColors dg)
                ++ [ "-- ---------------------------------------------------------------------------"
                   , "-- Skin tones"
                   , "-- ---------------------------------------------------------------------------"
                   , ""
                   , ""
                   , "type alias SkinTone ="
                   , "    { id : String, name : String, hex : String }"
                   , ""
                   , ""
                   , "skinTones : List SkinTone"
                   , "skinTones ="
                   , renderSkinToneList (dgSkinTones dg)
                   , ""
                   , ""
                   , ""
                   , "-- ---------------------------------------------------------------------------"
                   , "-- Rainbow"
                   , "-- ---------------------------------------------------------------------------"
                   , ""
                   , ""
                   , "type alias RainbowColor ="
                   , "    { id : String, name : String, hex : String, description : String }"
                   , ""
                   , ""
                   , "rainbowColors : List RainbowColor"
                   , "rainbowColors ="
                   , renderRainbowList (dgRainbowColors dg)
                   , ""
                   , ""
                   , ""
                   , "-- ---------------------------------------------------------------------------"
                   , "-- Typography"
                   , "-- ---------------------------------------------------------------------------"
                   , ""
                   , ""
                   , "fontFamily : String"
                   , "fontFamily ="
                   , "    " <> q fontStack
                   , ""
                   , ""
                   , "fontFile : String"
                   , "fontFile ="
                   , "    " <> q ("/" <> tcFontFile tc)
                   , ""
                   , ""
                   , "type alias TypeStyle ="
                   , "    { name : String"
                   , "    , weight : Int"
                   , "    , sizePx : Int"
                   , "    , sizeRem : Float"
                   , "    , lineHeight : Float"
                   , "    , letterSpacingEm : Float"
                   , "    , cssClass : String"
                   , "    , description : String"
                   , "    }"
                   , ""
                   , ""
                   , "typeScale : List TypeStyle"
                   , "typeScale ="
                   , renderTypeScaleList (tcScale tc)
                   ]
                ++ concatMap renderTypeHelper (tcScale tc)
                ++ [ ""
                   , ""
                   , ""
                   , "-- ---------------------------------------------------------------------------"
                   , "-- Spacing"
                   , "-- ---------------------------------------------------------------------------"
                   , ""
                   , ""
                   , "spacingBaseUnit : Int"
                   , "spacingBaseUnit ="
                   , "    " <> T.pack (show (spcBaseUnit sp))
                   , ""
                   , ""
                   , "type alias SpacingStep ="
                   , "    { name : String, px : Int, rem : Float, tailwindClass : String, description : String }"
                   , ""
                   , ""
                   , "spacingScale : List SpacingStep"
                   , "spacingScale ="
                   , renderSpacingList (spcScale sp)
                   , ""
                   , ""
                   , "pageWrapper : String"
                   , "pageWrapper ="
                   , "    " <> q (spcPageWrapperClass sp)
                   , ""
                   , ""
                   , ""
                   , "-- ---------------------------------------------------------------------------"
                   , "-- Motion"
                   , "-- ---------------------------------------------------------------------------"
                   , ""
                   , ""
                   ]
                ++ concatMap renderMotionDuration (mcDurations mc)
                ++ concatMap renderMotionEasing (mcEasings mc)
                ++ [ ""
